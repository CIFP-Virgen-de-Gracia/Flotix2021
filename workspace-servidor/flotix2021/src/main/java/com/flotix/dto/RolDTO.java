package com.flotix.dto;

/**
 * Objeto que almacena la informacion de los roles para su devolucion
 * 
 * @author Flor
 *
 */
public class RolDTO {

	private String id;
	private String nombre;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
